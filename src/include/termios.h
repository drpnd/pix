#define B75     75
#define B110    110
#define B134    134
#define B150    150
#define B200    200
#define B300    300
#define B600    600
#define B1200   1200
#define B1800   1800
#define B2400   2400
#define B4800   4800
#define B9600   9600
#define B19200  19200
#define B38400  38400

struct termios;

int tcgetattr(int fildes, struct termios *termios_p);
int
tcsetattr(int fildes, int optional_actions, const struct termios *termios_p);


#endif /* _TERMIOS_H */

